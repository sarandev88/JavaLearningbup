package conditionalstatements;

public class Switchcase {
	public static void main(String[] args) {
		int blue = 7892;
		switch (blue) {
		case 5678:
			System.out.println("Software");
			break;
		case 8963:
		System.out.println("QA");
		break;
		case 8964:
			System.out.println("BPO");
			break;
		case 7892:
			System.out.println("HR");
			break;
		}
	}

}
