package constructors;

public class Defconst {
	String name;   // default value for wrapper class is null
	int a;         // primitive datattype value is default one  0
	
	
	public static void main(String[] args) {
		Defconst dc = new Defconst();
		System.out.println(dc.name);
		System.out.println(dc.a);
		
		
	}

}
